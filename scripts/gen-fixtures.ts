import { zlibSync } from 'fflate';
import { writeFile } from 'node:fs/promises';

function crc32(data: Uint8Array): number {
  let crc = 0xffffffff;
  for (let i = 0; i < data.length; i++) {
    crc ^= data[i]!;
    for (let j = 0; j < 8; j++) {
      crc = (crc >>> 1) ^ (crc & 1 ? 0xedb88320 : 0);
    }
  }
  return (crc ^ 0xffffffff) >>> 0;
}

function makeChunk(type: string, data: Uint8Array): Uint8Array {
  const len = new Uint8Array(4);
  new DataView(len.buffer).setUint32(0, data.length);
  const typeBytes = new TextEncoder().encode(type);
  const crcInput = new Uint8Array(typeBytes.length + data.length);
  crcInput.set(typeBytes);
  crcInput.set(data, typeBytes.length);
  const crcVal = crc32(crcInput);
  const crcBytes = new Uint8Array(4);
  new DataView(crcBytes.buffer).setUint32(0, crcVal);
  const chunk = new Uint8Array(12 + data.length);
  chunk.set(len, 0);
  chunk.set(typeBytes, 4);
  chunk.set(data, 8);
  chunk.set(crcBytes, 8 + data.length);
  return chunk;
}

function makePng(
  w: number,
  h: number,
  colorType: number,
  channels: number,
  pixelFn: (x: number, y: number) => number[],
): Uint8Array {
  const raw = new Uint8Array(h * (1 + w * channels));
  for (let y = 0; y < h; y++) {
    const rowOffset = y * (1 + w * channels);
    raw[rowOffset] = 0; // no filter
    for (let x = 0; x < w; x++) {
      const px = rowOffset + 1 + x * channels;
      const vals = pixelFn(x, y);
      for (let c = 0; c < channels; c++) {
        raw[px + c] = vals[c]!;
      }
    }
  }

  const ihdr = new Uint8Array(13);
  const ihdrV = new DataView(ihdr.buffer);
  ihdrV.setUint32(0, w);
  ihdrV.setUint32(4, h);
  ihdr[8] = 8;
  ihdr[9] = colorType;

  const signature = new Uint8Array([0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a]);
  const ihdrChunk = makeChunk('IHDR', ihdr);
  const idatChunk = makeChunk('IDAT', zlibSync(raw));
  const iendChunk = makeChunk('IEND', new Uint8Array(0));

  const png = new Uint8Array(
    signature.length + ihdrChunk.length + idatChunk.length + iendChunk.length,
  );
  let off = 0;
  png.set(signature, off);
  off += signature.length;
  png.set(ihdrChunk, off);
  off += ihdrChunk.length;
  png.set(idatChunk, off);
  off += idatChunk.length;
  png.set(iendChunk, off);
  return png;
}

